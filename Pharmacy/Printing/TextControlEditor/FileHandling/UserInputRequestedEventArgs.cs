using System;

namespace TextControlEditorPharmacyClient.FileHandling
{
	/*------------------------------------------------------------------------------------------------
	** E N U M S
	**----------------------------------------------------------------------------------------------*/
	public enum UserInputRequestReason {
		Unknown,
		PdfUserPassword,
	}

	/*------------------------------------------------------------------------------------------------
	** C L A S S
	**----------------------------------------------------------------------------------------------*/
	public class UserInputRequestedEventArgs : EventArgs {

		/*------------------------------------------------------------------------------------------------
		** C O N S T R U C T O R
		**----------------------------------------------------------------------------------------------*/

		public UserInputRequestedEventArgs(
			string current,
			string caption,
			string label,
			bool isPasswordRequest = false,
			UserInputRequestReason reason = UserInputRequestReason.Unknown) {

			Value = current;
			Caption = caption;
			Label = label;
			IsPasswordRequest = isPasswordRequest;
			DialogResult = FileHandling.DialogResult.Cancel;
		}

		/*------------------------------------------------------------------------------------------------
		** P U B L I C   P R O P E R T I E S
		**----------------------------------------------------------------------------------------------*/

		/*------------------------------------------------------------------------------------------------
		** Value
		**----------------------------------------------------------------------------------------------*/
		public string Value { get; set; }

		/*------------------------------------------------------------------------------------------------
		** Caption
		**----------------------------------------------------------------------------------------------*/
		public string Caption { get; private set; }

		/*------------------------------------------------------------------------------------------------
		** Label
		**----------------------------------------------------------------------------------------------*/
		public string Label { get; private set; }

		/*------------------------------------------------------------------------------------------------
		** IsPasswordRequest
		**----------------------------------------------------------------------------------------------*/
		public bool IsPasswordRequest { get; private set; }

		/*------------------------------------------------------------------------------------------------
		** Reason
		**----------------------------------------------------------------------------------------------*/
		public UserInputRequestReason Reason { get; private set; }

		/*------------------------------------------------------------------------------------------------
		** DialogResult
		**----------------------------------------------------------------------------------------------*/
		public FileHandling.DialogResult DialogResult { get; set; }
	}
}
